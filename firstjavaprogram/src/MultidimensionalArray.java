public class MultidimensionalArray {
    public static void main(String[] args)
    {
        //2-D
        String[][] arr= {
                {" hello ", " hi "},
                {" world "," japan "," nepal "},
                {"k xa"}
        };
        System.out.println(arr[0][1]+arr[1][1]);
        System.out.println(arr[0][1]+arr[1][1]+arr[2][0]);
        System.out.println(arr[0][0]+arr[1][2]);

        //3-D

       ;
    }
}
